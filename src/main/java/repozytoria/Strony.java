package repozytoria;

public class Strony {
	
	private int strona;
	private int RozmiarStrony;
	private int Razem;
	
	
	
	public int getStrona() {
		return strona;
	}
	public void setStrona(int strona) {
		this.strona = strona;
	}
	public int getRozmiarStrony() {
		return RozmiarStrony;
	}
	public void setRozmiarStrony(int rozmiarStrony) {
		this.RozmiarStrony = rozmiarStrony;
	}
	public int getRazem() {
		return Razem;
	}
	public void setRazem(int razem) {
		this.Razem = razem;
	}

	
	
}
